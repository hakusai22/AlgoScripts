class Solution:
    def findTheLongestBalancedSubstring(self, s: str) -> int:
        arr = list(s)
        res = 0
        i = 0
        while i < len(arr):
            c0 = 0
            c1 = 0
            while i < len(arr) and arr[i] == '0':
                i += 1
                c0 += 1

            while i < len(arr) and arr[i] == '1':
                c1 += 1
                i += 1
            res = max(res, min(c0, c1))

        return res * 2
